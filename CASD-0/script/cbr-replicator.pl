# log
#
# Put an entry in the log file

sub log {
  my ($entry) = @_;

  my ($s, $m, $h, $d, $mo, $y) = localtime;
  $mo++;
  $y += 1900;

  print LOG sprintf("%04d%02d%02d-%02d%02d%02d $entry\n",
		    $y, $mo, $d, $h, $m, $s);
}

# usage
#
# Print a usage message to STDERR and then exit

sub usage {
  print STDERR <<USAGE;

This is a program to make multiple runs of a model of social games using agents
with case-based reasoning for their cognitive architecture and then analyse the
output of those runs.

Usage:
\t$0 <mode> [<runs> | <filename>] <prog> <param> [<cmd params...>]

\t\t<mode>         'Random' or 'Deterministic' 
\t\t               (use Deterministic only if forwardMemory is 1).
\t\t<runs>         If mode is 'Random' specify the number of runs
\t\t<filename>     If mode is 'Random' specify a file with seeds
\t\t<prog>         Full path to model program to use
\t\t<param>        Parameter file to use with the model
\t\t<cmd param...> Command line parameters to send to the program
\t\t\tThese should be with all + (model specific) options
\t\t\tfirst then - (Swarm specific) options last.
USAGE
  &log("Error!");
  close(LOG);
  exit 1;
}

# get_model_swarm_param(model, swarm, args)
#
# Separate the model specific command line options and the swarm specific
# command line options from the list of arguments supplied.

sub get_model_swarm_param {
  my ($model, $swarm, @args) = @_;

  my $cmdmode = 'model';
  my $firstswarmarg = 'undef';

  foreach my $arg (@args) {
    if($cmdmode eq 'swarm') {
      if($arg =~ /^\+/) {
	print STDERR "Model command line argument $arg after Swarm argument "
	  ."begins ($firstswarmarg)\n";
	&usage;
      }
    }
    elsif($arg =~ /^-/) {
      $firstswarmarg = $arg;
      $cmdmode = 'swarm';
    }

    if($cmdmode eq 'model') {
      push(@$model, $arg);
    }
    else {
      if($arg eq '-S' || $arg eq '-s') {
	print STDERR "You cannot pass seed arguments to Swarm from the "
	  ."command line to this program, as it will take care of this.\n";
	&usage;
      }
      push(@$swarm, $arg);
    }
  }
}

# get_cd_string_length(paramfile)
#
# Read the parameter file to get the parameters determining the string length
# then calculate the string length and return it.

sub get_cd_string_length {
  my ($paramfile) = @_;

  open(PARAM, "<$paramfile") or die "Cannot open $paramfile for reading: $!\n";

  &log("Reading parameters from file $paramfile");
  my %var;
  while(my $line = <PARAM>) {
    chomp $line;
    if($line =~ /^\s+\#:(\S+)\s+(\S+)$/) {
      $var{$1} = $2;
      &log("Read parameter $1 = $2");
    }
  }
  close(PARAM);

  my $error = 0;
  foreach my $needed_var ('xSize', 'ySize', 'descriptorOtherDefectorsStr',
			  'descriptorMyDecisionsStr', 'bMemory', 'fMemory') {
    if(!defined($var{$needed_var})) {
      print STDERR ", " if $error;
      print STDERR $needed_var;
      $error = 1;
    }
  }

  if($error) {
    print STDERR " not found in parameter file $paramfile\n";
    usage;
  }

  my $nAgentsPow;
  my $twoPow;
  if($var{'descriptorOtherDefectorsStr'} eq 'NO') {
    $nAgentsPow = 0;
  }
  elsif($var{'descriptorOtherDefectorsStr'} eq 'YES') {
    $nAgentsPow = $var{'bMemory'};
  }
  else {
    print STDERR "Invalid setting for descriptorOtherDefectorsStr parameter:"
      ." $var{'descriptorOtherDefectorsStr'}\n";
    &usage;
  }

  if($var{'descriptorMyDecisionsStr'} eq 'NO') {
    $twoPow = 0;
  }
  elsif($var{'descriptorMyDecisionsStr'} eq 'YES') {
    $twoPow = $var{'bMemory'};
  }
  else {
    print STDERR "Invalid setting for descriptorMyDecisionsStr parameter:"
      ." $var{'descriptorMyDecisionsStr'}\n";
    &usage;
  }

  my $nAgents = $var{'xSize'} * $var{'ySize'};
  return ($nAgents * ($var{'bMemory'}
		      + (2 ** $twoPow) * ($nAgents ** $nAgentsPow)));
}

